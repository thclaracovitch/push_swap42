#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*c1;
	unsigned char	*c2;

	c1 = (unsigned char *)s1;
	c2 = (unsigned char *)s2;
	if (!n)
		return (0);
	else if (*c1 != *c2)
		return (*c1 - *c2);
	return (ft_memcmp(++s1, ++s2, --n));
}
